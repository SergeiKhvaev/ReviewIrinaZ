package day22arrays;

import java.util.Arrays;

public class ExampleArrayDouble {
    public static void main(String[] args) {

        double[] prices = new double[4];
        System.out.println(Arrays.toString(prices));

        prices[0] = 12.99;
        prices[1] = 1.99;
        prices[3] = 2.59;
        System.out.println(Arrays.toString(prices));
        prices[3] = 100;
        System.out.println(Arrays.toString(prices));


















    }
}
