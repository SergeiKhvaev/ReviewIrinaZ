package day18_whale_loop_practice;

public class WhileLoopUp500 {

    public static void main(String[] args) {
        // печатать от 1 - 500

        int step = 1;
        int limit = 500;

        while (step <= limit){
            System.out.print(step + " ");
            step = step +1;
        }






    }
}
